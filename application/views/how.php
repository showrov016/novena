“Settle with your opponent quickly while on the way to court with him, Otherwise your opponent will hand you over to the judge, and the judge will hand you over to the guard, and you will be thrown into prison; Amen I say to you, you will not be released until you have paid the last penny.”   Matt 5:25-26 & Luke 12:58-59<br><br>

“We kept saying to ourselves how can he pay the debt he owes if he’s in prison?”  Something just doesn’t make sense.  Then the pieces of the puzzle began coming together—the light of the Holy Spirit finally broke through.  We finally realized and understood how this debt could be paid.  The prisoner reflects the souls in purgatory—after all he can only get out if the debt is paid. (Those who are alive, intercessors; meaning all of us who are praying can help pay the debt) <br><br>

Here are a couple of examples: <br><br>

Dick and Jane were playing ball outside and accidentally broke the neighbor’s window.  They went home crying to their parents that it was an accident and they were sorry (confession: although, not sacramental as there was no sin).  The parents accepted their apology but they still had to pay for the window. The children could not pay the debt (like the prisoner) but gratefully their parents were able to pay.  (purgatory: the children who couldn’t pay their debt)  (intercessors:  are the parents who can paid their debt). <br><br>

An uninsured family lost everything due to a house fire caused by a careless smoker. Neither the family nor the smoker had the ability to restore what was lost. The smoker was very upset and remorseful, however even the remorse and guilt could not restore what was lost. The community, however, came together out of love, and rebuilt the house.  Although he was not physically imprisoned, the internal pain and guilt the smoker experienced (purgatory) was overwhelming.  It was the generosity of the community (intercessors) who restored what was lost; and to the homeowners (representing God as in a parable) justice was indeed satisfied.<br><br>

Notice in both examples the guilty parties could not pay the debt, but due to the generosity of the intercessors (we, the living) the debts were paid in full. But, the most wonderful thing about our prayers (this novena) as intercessors is that God accepts the payment, much like most of us would, from whoever is willing to make it---Is that cool or what?</p>


