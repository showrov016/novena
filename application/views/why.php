God’s criteria for heaven: Holiness.<br><br>

The Catechism of the Catholic Church, 1054, states “those who die in God’s grace and friendship imperfectly purified, although they are assured of their salvation, undergo purification after death, so as to achieve the holiness necessary to enter the joy of God”.<br><br>

Could you imagine a famous guest coming to your house for dinner and they arrived a week early.  You probably wouldn’t even want to answer the door as you wanted everything perfect before they arrived.  This life is our opportunity to be prepared daily….as we do not know the time or date that Our Lord will return.  It’s our prayer that when Jesus comes to meet us we have lived His message of merciful love and forgiveness; and we’re properly dressed and do not have to suffer the punishment described in “the parable of the wedding banquet” Matt 22:11 & following.    One thing is certain:  Jesus will return either at the end of our earthly life or the end of the world, whichever comes first.  The message of “be prepared” is always timely. If you are not prepared, Purgatory is the place where the purification is completed. First Corinthians 3:15 “But if someone’s work is burned up, that one will suffer loss; the person will be saved, but only as through fire.” <br><br>

Why a Novena when there are so many prayers for the deceased?<br><br>

This novena in no way minimizes THE most important of all prayers, The Holy Sacrifice of the Mass.  The sacrifice of Jesus to the Father, in the Holy Sacrifice of the Mass is the most efficacious of all prayers for the release of a soul from purgatory.  Thus we encourage not only the Gregorian masses, but masses said as often as possible for the soul of your loved one.  However, in addition to all the prayers, this novena is a beautiful gift to pray DAILY for the release of the soul of your loved one, as it is designed for that one special person for nine consecutive days. Prayerfully this novena will enlighten the hearts of the family and friends of the deceased and reaffirm a sometimes neglected doctrine.  It is a way of praying for your loved one for nine days following their death by invoking the whole heavenly body to join with your prayer.  It is a supreme act of love as we now understand that the souls cannot pray for themselves.  St. Faustina wrote, “I saw my guardian angel, who ordered me to follow him.  In a moment I was in a misty place full of fire in which there was a great crowd of suffering souls. They were praying fervently, but to no avail, for themselves; ONLY WE CAN COME TO THEIR AID”. The holy souls are ever grateful for our prayerful assistance in their delivery from purgatory.  They will certainly intercede before the throne of God to obtain the many favors that we ask of them.  St. Teresa of Jesus stated “I always obtain the favors, which I ask from God, through the intercession of the poor souls.”  Two modern day examples of holiness are Mother Theresa of Calcutta and Pope John Paul II.  Both of these holy people requested prayers to be offered after their deaths (to release their souls from purgatory).  To concretely show our love for these holy souls we prayed for them until they were beatified.  Now we ask them to pray for us!</p>


